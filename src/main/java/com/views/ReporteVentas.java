    public void cargarReportes() {
        // 1. Limpiar filas anteriores
        modeloreportes.setRowCount(0); 
        try {
            // 2. Obtener la lista de clientes activos
            List<ReporteVenta> reportes = reportedao.obtenerReporteVentasDetallado();
            // 3. Iterar sobre la lista y a√±adir cada cliente como una fila
            for (ReporteVenta reporte : reportes) {
                Object[] rowData = new Object[8];
                rowData[0] = reporte.getFechaDisplay();
                rowData[1] = reporte.getFolio_v();
                rowData[2] = reporte.getEmpleado();
                rowData[3] = reporte.getCliente();
                rowData[4] = reporte.getProducto();
                rowData[5] = reporte.getCantidad();
                rowData[6] = reporte.getPrecio_u();
                rowData[7] = reporte.getTotal_venta();

                modeloreportes.addRow(rowData);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al cargar la lista de clientes: " + e.getMessage(), 
                "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    

