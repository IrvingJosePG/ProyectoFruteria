    public void cargarReporteCompras() {
        // Limpiar filas anteriores
        modeloCompras.setRowCount(0); 

        try {
            // 1. Obtener la lista de ReporteCompra usando el DAO (Consulta de 5 tablas)
            List<ReporteCompra> reportes = compraDAO.listarReporteDetalleCompras();

            // 2. Iterar sobre la lista y a√±adir cada reporte como una fila
            for (ReporteCompra rc : reportes) {
                Object[] rowData = new Object[8];
                rowData[0] = rc.getFolioC();
                rowData[1] = rc.getFechaDisplay();
                rowData[2] = rc.getNombreProveedor();
                rowData[3] = rc.getNombreEmpleado();
                rowData[4] = rc.getDescripcionProducto();
                rowData[5] = rc.getCantidad();
                rowData[6] = String.format("$%.2f", rc.getCostoUnitario()); // Formato de moneda
                rowData[7] = String.format("$%.2f", rc.getTotal());        // Total calculado

                modeloCompras.addRow(rowData);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar el historial de compras: " + e.getMessage(), "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
