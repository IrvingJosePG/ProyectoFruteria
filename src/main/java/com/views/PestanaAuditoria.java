    public void configurarTabla() {
        String[] columnNames = {"ID", "Tabla Afectada", "Operacion", "UsuarioBD", "Fecha Auditoria"};

        this.modeloauditoria = new DefaultTableModel(columnNames, 0) {
            // Hacemos que la tabla no sea editable directamente
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tablaauditorias.setModel(this.modeloauditoria);
        tablaauditorias.getTableHeader().setResizingAllowed(true);

        tablaauditorias.getColumnModel().getColumn(0).setPreferredWidth(60);
        tablaauditorias.getColumnModel().getColumn(1).setPreferredWidth(155);
        tablaauditorias.getColumnModel().getColumn(2).setPreferredWidth(155);
        tablaauditorias.getColumnModel().getColumn(3).setPreferredWidth(155);
        tablaauditorias.getColumnModel().getColumn(4).setPreferredWidth(155);

    }

    public void cargarAuditorias() {
        // 1. Limpiar filas anteriores
        modeloauditoria.setRowCount(0);
        try {
            // 2. Obtener la lista de clientes activos
            List<Auditoria> auditorias = auditoriadao.obtenerAuditoria();
            // 3. Iterar sobre la lista y a√±adir cada cliente como una fila
            for (Auditoria au : auditorias) {
                Object[] rowData = new Object[5];
                rowData[0] = au.getId_a();
                rowData[1] = au.getNombretabla();
                rowData[2] = au.getOperacion();
                rowData[3] = au.getUsuariobd();
                rowData[4] = au.getFechaDisplay();

                modeloauditoria.addRow(rowData);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al cargar la lista de clientes: " + e.getMessage(),
                    "Error de Base de Datos", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

